import UIKit


extension UIColor {
    public class var lightBlue: UIColor {
        return UIColor(red: 51/255, green: 135/255, blue: 238/255, alpha: 1.0)
    }
   
    public class var lightGrey: UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 151/255, alpha: 1.0)
    }
    
    public class var black: UIColor {
        return UIColor(red: 47/255, green: 47/255, blue: 49/255, alpha: 1.0)
    }
    
    public class var white: UIColor {
        return UIColor(red:223/255, green:230/255, blue: 233/255, alpha: 1.0)
    }
}
